    // Small tile images
    private Image grass, path, house, water, tree, arena, bush;

    // Delegate for large tiles
    private LargeTileManager largeTileManager;

    public TileMap() {
        loadSmallTiles();
        largeTileManager = new LargeTileManager(this);
    }

    private void loadSmallTiles() {
        grass = new Image(getClass().getResourceAsStream("/tiles/plainvar.png"));
        path = new Image(getClass().getResourceAsStream("/tiles/dirt.png"));
        house = new Image(getClass().getResourceAsStream("/tiles/plainvar.png"));
        water = new Image(getClass().getResourceAsStream("/tiles/water.png"));
        arena = new Image(getClass().getResourceAsStream("/tiles/water.png"));
    }

    // Main render method - orchestrates everything
    public void renderFullMap(GraphicsContext gc) {
        drawBackground(gc);  // Background layer
        drawForeground(gc);   // Foreground layer
        largeTileManager.renderAllLargeTiles(gc);  // Buildings, trees, etc.
    }

    // Draws background tilemap (mostly grass)
    public void drawBackground(GraphicsContext gc) {
        for (int y = 0; y < townMapBackground.length; y++) {
            for (int x = 0; x < townMapBackground[0].length; x++) {
                Image tileImage = getTileImage(townMapBackground[y][x]);
                gc.drawImage(tileImage, (x + virtualOffsetX) * TILE_SIZE, (y + virtualOffsetY) * TILE_SIZE, TILE_SIZE + 10, TILE_SIZE + 10);
            }
        }
    }

    // Draws foreground tilemap (paths, water, etc.)
    public void drawForeground(GraphicsContext gc) {
        for (int y = 0; y < townMapForeGround.length; y++) {
            for (int x = 0; x < townMapForeGround[0].length; x++) {
                Image tileImage = getTileImage(townMapForeGround[y][x]);
                gc.drawImage(tileImage, x * TILE_SIZE, y * TILE_SIZE, TILE_SIZE + 10, TILE_SIZE + 10);
            }
        }
    }

    // Maps tile numbers to images
    private Image getTileImage(int tile) {
        return switch (tile) {
            case 1 -> path;
            case 2 -> house;
            case 3 -> water;
            case 4 -> tree;
            case 5 -> arena;
            default -> grass;
        };
    }

    // Getters
    public int getTileSize() {
        return TILE_SIZE;
    }

    // Check if a tile position has collision
    public boolean isSolid(int tileX, int tileY) {
        // Check bounds
        if (tileX < 0 || tileY < 0 || tileY >= collisionMap.length || tileX >= collisionMap[0].length) {
            return true; // Out of bounds = solid
        }
        return collisionMap[tileY][tileX] == 1;
    }

    // Check collision based on pixel coordinates
    public boolean isPositionBlocked(double x, double y) {
        int tileX = (int) (x / TILE_SIZE);
        int tileY = (int) (y / TILE_SIZE);
        return isSolid(tileX, tileY);
    }

    public int[][] getTownMapBackground() {
        return townMapBackground;
    }

    public int[][] getTownMapForeGround() {
        return townMapForeGround;
    }
